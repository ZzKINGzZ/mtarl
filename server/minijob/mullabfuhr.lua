		local rand = math.random(#mulltonne)
		
		if mulltonne[rand] then

			local randModel = math.random(#mullmodels)
			local mullmodel = mullmodels[randModel]
			
			local object
			if mullmodel == 1337 or mullmodel == 1339 or mullmodel == 1343  or mullmodel == 1344 or mullmodel == 1345 then
				object = createObject(mullmodel, mulltonne[rand][1], mulltonne[rand][2], mulltonne[rand][3]-0.3)
			elseif mullmodel == 1347 or mullmodel == 1371 or mullmodel == 1265 then
				object = createObject(mullmodel, mulltonne[rand][1], mulltonne[rand][2], mulltonne[rand][3]-0.4)
			elseif mullmodel == 1439 then
				object = createObject(mullmodel, mulltonne[rand][1], mulltonne[rand][2], mulltonne[rand][3]-0.8)
			elseif mullmodel == 1574 then
				object = createObject(mullmodel, mulltonne[rand][1], mulltonne[rand][2], mulltonne[rand][3]-1)
			elseif mullmodel == 1236 then
				object = createObject(mullmodel, mulltonne[rand][1], mulltonne[rand][2], mulltonne[rand][3]-0.35)
			elseif mullmodel == 1336 or mullmodel == 1334 or mullmodel == 1333  or mullmodel == 1332 or mullmodel == 1331 then
				object = createObject(mullmodel, mulltonne[rand][1], mulltonne[rand][2], mulltonne[rand][3]-0.1)
