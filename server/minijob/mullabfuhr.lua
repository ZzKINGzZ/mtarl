function destroyTempCarMull()
	if g_playerstat[source]["Jobvar"]["Mull"] then
		local vehicle = g_playerstat[source]["Jobvar"]["Mull"]["Car"]
		if g_vehiclestat[vehicle]["tempcar"] == true then
			if g_vehiclestat[vehicle]["MullCar"] == true then
				deleteTempVehicleMTARL(vehicle)
			end
		end
	end
end
addEventHandler("onPlayerLogoutMTARL", getRootElement(), destroyTempCarMull)

--MTA:RL Internes Event
function timerMull()
	if g_playerstat[source]["Jobvar"]["Mull"] then
		if g_playerstat[source]["Jobvar"]["Mull"]["Timer"] > 0 then
			g_playerstat[source]["Jobvar"]["Mull"]["Timer"] = g_playerstat[source]["Jobvar"]["Mull"]["Timer"] - 1
			
			if g_playerstat[source]["Jobvar"]["Mull"]["Timer"] == 0 then
				sendPlayerInfo(source, "Du hast den Job abgebrochen!")
				g_playerstat[source]["Jobvar"]["Mull"]["Timer"] = -1
				local vehicle = g_playerstat[source]["Jobvar"]["Mull"]["Car"]
				if vehicle then					
					--L"..ouml.."schen wir das Fahrzeug
					if g_vehiclestat[vehicle]["tempcar"] == true then
						if g_vehiclestat[vehicle]["MullCar"] == true then
							deleteTempVehicleMTARL(vehicle)
						end
					end
				end
				
				setElementModel(source, g_playerstat[source]["Skin"])				
					
				--Jobvars reseten
				g_playerstat[source]["Jobvar"]["Mull"] = nil
				
				--Job verlassen, ohne ihm Geld zu geben und ihm das mitzuteilen
				playerLeaveJob(source, 14, 1, false, true)					
			end
		end
	end
end
addEventHandler("onGeneralPlayerTimer", getRootElement(), timerMull)

